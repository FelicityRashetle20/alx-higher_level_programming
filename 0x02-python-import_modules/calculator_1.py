#!/usr/bin/python3
def add(a, b):
    """Module that returns the total of a and b
    """
    return (a + b)


def sub(a, b):
    """Module that returns the value of a subtracted by b
    """
    return (a - b)

def div(a, b):
    """Module that returns the value of a divided by b
    """
    return int(a / b)

def mul(a, b):
    """Module that returns the value of a multiplied by b
    """
    return (a * b)
